// 2. Bit Mask - II
public class Solution {
    public int hammingWeight(int n) {
        // res 用來累加 n 的二進位表示中「1」的個數
        int res = 0;

        // 當 n 不為 0，就持續檢查最低位並右移
        // 注意：這裡使用的是算術右移 >>，會用符號位補值（若 n 為負數會一直補 1）
        // 在題目假設（無號 32-bit）下通常建議用 >>>，但本程式碼依照你的要求不做修改
        while (n != 0) {
            // 取出 n 的最低位（n & 1），若為 1 則加 1，否則加 0（res++）
            // 這行等價於：res += (n & 1); 但依你的原寫法保留三元運算子
            res += (n & 1) == 1 ? 1 : 0;

            // 右移一位，丟棄已檢查過的最低位 (相當於把 n 的二進位整體往右搬一格，最低位丟掉)
            // 這裡使用算術右移（>>），若 n 為負數，最高位會以 1 填補，可能導致無限迴圈
            n >>= 1;
        }

        // 回傳 1 的總數
        return res;
    }
}