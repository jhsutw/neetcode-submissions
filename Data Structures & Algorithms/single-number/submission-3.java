// 4. Bit Manipulation
public class Solution {
    public int singleNumber(int[] nums) {
        // 使用位元「異或 XOR」來找只出現一次的數
        // 關鍵性質：
        // 1) x ^ x = 0（相同數字異或會互相抵消）
        // 2) x ^ 0 = x
        // 3) 異或具交換律、結合律（順序不影響結果）
        // 因此把所有元素依序 XOR 起來，成對的數會被抵消為 0，只剩下那個出現一次的數
        int res = 0;
        for (int num : nums) {
            res ^= num;
        }
        return res;
    }
}

/*
解題思路（只用註解版）：
- 使用位元異或 XOR 來找出只出現一次的數。
- 性質：
  1) x ^ x = 0   （相同數會互相抵消）
  2) x ^ 0 = x
  3) 交換律、結合律：a ^ b ^ a = (a ^ a) ^ b = 0 ^ b = b
- 把陣列所有元素依序 XOR 起來，成對的數都會被抵消成 0，最後只剩下那個唯一的數。

範例 1：
十進位
nums = [4, 1, 2, 1, 2]
res = 0
res ^= 4  -> 0 ^ 4 = 4
res ^= 1  -> 4 ^ 1 = 5
res ^= 2  -> 5 ^ 2 = 7
res ^= 1  -> 7 ^ 1 = 6   （兩個 1 抵消）
res ^= 2  -> 6 ^ 2 = 4   （兩個 2 抵消）
最終 res = 4  → 唯一出現一次的數

二進位
res = 0000
res ^ 0100 = 0100   (0 ^ 4)
res ^ 0001 = 0101   (4 ^ 1)
res ^ 0010 = 0111   (5 ^ 2)
res ^ 0001 = 0110   (7 ^ 1)   ← 1 和 1 抵消
res ^ 0010 = 0100   (6 ^ 2)   ← 2 和 2 抵消

範例 2（含負數也適用）：
nums = [-3, -3, 5]
res = 0
res ^= -3 -> 0 ^ (-3) = -3
res ^= -3 -> -3 ^ (-3) = 0  （成對抵消）
res ^= 5  -> 0 ^ 5 = 5
最終 res = 5

複雜度：
- 時間 O(n)，一次線性掃描
- 空間 O(1)，只用到一個累積變數 res
*/
